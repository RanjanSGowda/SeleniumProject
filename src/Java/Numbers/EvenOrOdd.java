package Java.Numbers;

//Using % operator

public class EvenOrOdd {
    public static void main(String[] args) {
        int num=10;

        if(num%2==0)
        {
            System.out.println("The given number "+ num +" is Even number");
        }
        else {
            System.out.println("The given number "+ num +" is Odd number");
        }
    }
}

